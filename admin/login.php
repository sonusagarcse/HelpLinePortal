<?php
session_start();

// Redirect if already logged in
if (isset($_SESSION['admin_logged_in']) && $_SESSION['admin_logged_in'] === true) {
    header('Location: index.php');
    exit;
}

require_once(__DIR__ . '/config/config.php');

// Generate CSRF token if not exists
if (empty($_SESSION['csrf_token'])) {
    $_SESSION['csrf_token'] = bin2hex(random_bytes(32));
}

$error = '';

if ($_SERVER['REQUEST_METHOD'] === 'POST') {
    // Validate CSRF token
    if (!isset($_POST['csrf_token']) || $_POST['csrf_token'] !== $_SESSION['csrf_token']) {
        die('CSRF token validation failed');
    }

    $email = mysqli_real_escape_string($con, $_POST['email']);
    $password = $_POST['password'];

    // Query admin_login table
    $query = "SELECT * FROM admin_login WHERE email = ? AND status = 1";
    $stmt = mysqli_prepare($con, $query);
    mysqli_stmt_bind_param($stmt, "s", $email);
    mysqli_stmt_execute($stmt);
    $result = mysqli_stmt_get_result($stmt);

    if ($row = mysqli_fetch_assoc($result)) {
        // Verify hashed password
        if (password_verify($password, $row['pass'])) {
            // Regenerate session ID for security
            session_regenerate_id(true);

            // Set session variables
            $_SESSION['admin_logged_in'] = true;
            $_SESSION['admin_id'] = $row['id'];
            $_SESSION['admin_name'] = $row['uname'];
            $_SESSION['admin_email'] = $row['email'];
            $_SESSION['admin_type'] = $row['utype'];
            $_SESSION['admin_bid'] = $row['bid'];
            $_SESSION['last_activity'] = time();

            // Log the login
            $ip = $_SERVER['REMOTE_ADDR'];
            $user_agent = $_SERVER['HTTP_USER_AGENT'];
            $log_query = "INSERT INTO admin_logs (admin_id, action, ip_address, user_agent) VALUES (?, 'login', ?, ?)";
            $log_stmt = mysqli_prepare($con, $log_query);
            mysqli_stmt_bind_param($log_stmt, "iss", $row['id'], $ip, $user_agent);
            mysqli_stmt_execute($log_stmt);

            header('Location: index.php');
            exit;
        } else {
            $error = 'Invalid email or password';
        }
    } else {
        $error = 'Invalid email or password';
    }
}
